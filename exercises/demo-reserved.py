# This script demonstrates the use of reserved characters in Python strings

print("Hello, World!")  # This is a comment
print("She said, \"Hello!\"")  # Using escape character for double quotes
print('It\'s a beautiful day!')  # Using escape character for single quote
print("Line1\nLine2")  # New line character
print("Column1\tColumn2")  # Tab character
print("Backslash: \\")  # Backslash character
print("Carriage Return Example\rCR")  # Carriage return
print("Form Feed Example\fFF")  # Form feed
print("Vertical Tab Example\vVV")  # Vertical tab
print("End of reserved characters demo")

